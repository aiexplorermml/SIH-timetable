// Helper functions
export const getMappingsByDepartment = (department) => {
  return subjectMapping.filter(mapping => mapping.department === department);
};

export const getMappingsByFaculty = (facultyId) => {
  return subjectMapping.filter(mapping => mapping.facultyId === facultyId);
};

export const getMappingsBySubject = (subjectId) => {
  return subjectMapping.filter(mapping => mapping.subjectId === subjectId);
};

export const getMappingsBySemester = (semester) => {
  return subjectMapping.filter(mapping => mapping.semester === semester);
};

export const getFacultySubjects = (facultyId) => {
  const mappings = getMappingsByFaculty(facultyId);
  return mappings.map(mapping => ({
    subjectId: mapping.subjectId,
    subjectName: mapping.subjectName,
    department: mapping.department,
    semester: mapping.semester,
    section: mapping.section
  }));
};